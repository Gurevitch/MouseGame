type BitmapFont struct {
	tex   *sdl.Texture
	charW int32
	charH int32
}

func NewBitmapFont(renderer *sdl.Renderer) *BitmapFont {
	cols := int32(16)
	rows := int32(6)
	atlasW := cols * glyphW
	atlasH := rows * glyphH

	surface, err := sdl.CreateRGBSurface(0, atlasW, atlasH, 32,
		0x000000FF, 0x0000FF00, 0x00FF0000, 0xFF000000)
	if err != nil {
		panic(err)
	}
	defer surface.Free()

	surface.FillRect(nil, sdl.MapRGBA(surface.Format, 0, 0, 0, 0))
	white := sdl.MapRGBA(surface.Format, 255, 255, 255, 255)

	for ch := byte(32); ch < 128; ch++ {
		idx := int32(ch - 32)
		baseX := (idx % cols) * glyphW
		baseY := (idx / cols) * glyphH
		glyph := font5x7[ch]
		for r := int32(0); r < glyphH; r++ {
			for c := int32(0); c < glyphW; c++ {
				if glyph[r]&(1<<uint(glyphW-1-c)) != 0 {
					surface.FillRect(&sdl.Rect{X: baseX + c, Y: baseY + r, W: 1, H: 1}, white)
				}
			}
		}
	}

	tex, err := renderer.CreateTextureFromSurface(surface)
	if err != nil {
		panic(err)
	}
	tex.SetBlendMode(sdl.BLENDMODE_BLEND)
	return &BitmapFont{tex: tex, charW: glyphW, charH: glyphH}
}

func (f *BitmapFont) DrawText(renderer *sdl.Renderer, text string, x, y, scale int32, color sdl.Color) {
	f.tex.SetColorMod(color.R, color.G, color.B)
	f.tex.SetAlphaMod(color.A)

	cx := x
	spacing := (f.charW + 1) * scale
	for i := 0; i < len(text); i++ {
		idx := int32(text[i]) - 32
		if idx < 0 || idx >= 96 {
			idx = int32('?') - 32
		}
		renderer.Copy(f.tex,
			&sdl.Rect{X: (idx % 16) * f.charW, Y: (idx / 16) * f.charH, W: f.charW, H: f.charH},
			&sdl.Rect{X: cx, Y: y, W: f.charW * scale, H: f.charH * scale},
		)
		cx += spacing
	}
}

func (f *BitmapFont) TextWidth(text string, scale int32) int32 {
	if len(text) == 0 {
		return 0
	}
	return int32(len(text)) * (f.charW + 1) * scale
}

// LineHeight returns the rendered glyph height in pixels for a given scale.
// Used by UI code that vertically centers text inside a fixed-height box
// (e.g. menu buttons) and needs the scaled glyph size without reaching into
// the font's private fields.
func (f *BitmapFont) LineHeight(scale int32) int32 {
	return f.charH * scale
}
